export type ColumnEditorTranslationKey = keyof typeof columnEditorTranslations['zh-TW'];

export const useColumnEditorTranslation = () => {
  const { language } = useTranslation();
  const t = (key: ColumnEditorTranslationKey) => {
    const dict = columnEditorTranslations[language] || columnEditorTranslations['zh-TW'];
    return dict[key] || key;
  };
  return { t };
};
